DOCKER_CMD=docker
GOLANGCI_LINT_IMAGE="golangci/golangci-lint:v1.54.2"

${DOCKER_CMD} run --rm -e GOPROXY=${GOPROXY} -v $(pwd):/opt/workspace -w /opt/workspace ${GOLANGCI_LINT_IMAGE} golangci-lint run --max-same-issues 20 --build-tags testing